    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Please provide proper id to view data","Warning",JOptionPane.ERROR_MESSAGE);
        }
        
        String id = jTextField1.getText();
        String dob = null;
        
        try
        {
            Doctor d = new Doctor();
            d.setId(id);
            ResultSet rs = DoctorDao.Display(d);
            
            if(rs.next() == true)
            {
                jTextField2.setText(rs.getString("firstname"));
                jTextField3.setText(rs.getString("lastname"));
                jTextArea1.setText(rs.getString("speciality"));
                dob = rs.getString("dob");
                Date date = new SimpleDateFormat("dd-MM-yyyy").parse(dob);
                jDateChooser1.setDate(date);
                jComboBox1.setSelectedItem(rs.getString("gender"));
                jTextField5.setText(rs.getString("contact"));
                jTextField6.setText(rs.getString("emailid"));
                jTextArea2.setText(rs.getString("permanentaddress"));
                jTextArea3.setText(rs.getString("presentaddress"));
            }
            else
            {
                JOptionPane.showMessageDialog(this,"No record found for id : "+id,"Error",JOptionPane.ERROR_MESSAGE);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jDateChooser1.setDateFormatString("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextArea1.setText("");
                jTextArea2.setText("");
                jTextArea3.setText("");
            }
        }
        catch(Exception E)
        {
            JOptionPane.showMessageDialog(this,E.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

