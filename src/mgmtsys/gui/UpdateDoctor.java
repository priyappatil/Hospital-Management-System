    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String s1 = jTextField5.getText();
        String s2 = jTextField6.getText();
        
        if(!s2.contains("@"))
        {
            JOptionPane.showMessageDialog(this,"Please provide proper emailid","Error",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            if(s1.length() != 10)
            {
                JOptionPane.showMessageDialog(this,"Please provide proper contact number","Error",JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                String id = jTextField1.getText();
                String firstname = jTextField2.getText();
                String lastname = jTextField3.getText();
                String speciality = jTextArea1.getText();
                String dob = ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
                String contact = jTextField5.getText();
                String gender = (String) jComboBox1.getSelectedItem();
                String emailid = jTextField6.getText();
                String permanentaddress = jTextArea2.getText();
                String presentaddress = jTextArea3.getText();

                try
                {
                    Doctor d = new Doctor();
                    d.setId(id);
                    d.setFirstname(firstname);
                    d.setLastname(lastname);
                    d.setSpeciality(speciality);
                    d.setDob(dob);
                    d.setContact(contact);
                    d.setGender(gender);
                    d.setEmailid(emailid);
                    d.setPermanentaddress(permanentaddress);
                    d.setPresentaddress(presentaddress);

                    updatedoctor = DoctorDao.updateDoctor(d);

                    if(updatedoctor == true)
                    {
                        JOptionPane.showMessageDialog(this,"Data is updated successfully","Message",JOptionPane.INFORMATION_MESSAGE);
                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextArea1.setText("");
                        //jTextField4.setText("");
                        jDateChooser1.setDateFormatString("");
                        jTextField5.setText("");
                        jTextField6.setText("");
                        jTextArea2.setText("");
                        jTextArea3.setText("");
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this,"Data cannot be updated","Error",JOptionPane.ERROR_MESSAGE);
                    }
                }
                catch(Exception E)
                {
                    JOptionPane.showMessageDialog(this,E.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

