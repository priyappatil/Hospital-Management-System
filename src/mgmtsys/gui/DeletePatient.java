    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try
        {
            int row = jTable1.getSelectedRow();
            String id = (String) jTable1.getValueAt(row,0);
            
            Patient p = new Patient();
            p.setId(id);
            
            deletepatient = PatientDao.deletePatient(p);
            
            if(deletepatient == true)
            {
                JOptionPane.showMessageDialog(this,"Record is deleted successfully","Message",JOptionPane.INFORMATION_MESSAGE);
                viewMethod();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Record cannot be deleted","Message",JOptionPane.INFORMATION_MESSAGE);
            }
        }
        catch(SQLException E)
        {
           JOptionPane.showMessageDialog(this, E,"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

