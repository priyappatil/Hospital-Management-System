    public void viewMethod() throws SQLException
    {
        try
        {
            ResultSet rs = PatientDao.viewData();

            DefaultTableModel m1 = (DefaultTableModel) jTable1.getModel();
            m1.setRowCount(0);

            while(rs.next())
            {
                m1.addRow(new Object[]
                {
                    rs.getString("id"),
                    rs.getString("firstname"),
                    rs.getString("lastname"),
                    rs.getString("disease"),
                    rs.getString("dob"),
                    rs.getString("gender"),
                    rs.getString("weight"),
                    rs.getString("bloodgroup"),
                    rs.getString("contact"),
                    rs.getString("permanentaddress"),
                    rs.getString("presentaddress"),
                });
            }
        }
        catch(SQLException E)
        {
            JOptionPane.showMessageDialog(this,E,"Error",JOptionPane.ERROR_MESSAGE);
        }
    }

